package library;

public class Book
{
	private String title;
	private String author;
	private String type;
	
	public String getTitle()
	{
		return title;
	}
	
	public String getAuthor()
	{
		return author;
	}
	
	public String getType()
	{
		return type;
	}
	
	public void setTitle(String n)
	{
		title = n;
	}
	
	public void setAuthor(String r)
	{
		author = r;
	}
	
	public void setType(String t)
	{
		type = t;
	}
}
